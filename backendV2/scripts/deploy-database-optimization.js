async function main() {
    try {
        const success = await executeDeployment();
        
        if (success) {
            console.log('\nüèÜ Database optimization deployment completed successfully!');
            console.log('Your ecommerce store now has Amazon-level database performance.');
            process.exit(0);
        } else {
            console.log('\nüí• Deployment failed. Please resolve critical issues before proceeding.');
            process.exit(1);
        }
        
    } catch (error) {
        console.error('\nüí• Deployment crashed:', error.message);
        process.exit(1);
    } finally {
        if (mongoose.connection.readyState === 1) {
            await mongoose.disconnect();
            console.log('üîå Disconnected from database');
        }
    }
}

// Handle process termination
process.on('SIGINT', async () => {
    console.log('\n‚ö†Ô∏è  Deployment interrupted by user');
    if (mongoose.connection.readyState === 1) {
        await mongoose.disconnect();
    }
    process.exit(0);
});

process.on('uncaughtException', async (error) => {
    console.error('\nüí• Uncaught exception during deployment:', error.message);
    if (mongoose.connection.readyState === 1) {
        await mongoose.disconnect();
    }
    process.exit(1);
});

// Run deployment
if (import.meta.url === `file://${process.argv[1]}`) {
    main().catch(console.error);
}

export default executeDeployment;
